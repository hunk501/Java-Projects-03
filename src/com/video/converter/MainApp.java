    private void selectFile(){
        filechooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        int val = filechooser.showOpenDialog(this);
        if(val == JFileChooser.APPROVE_OPTION){
            myfilename = filechooser.getSelectedFile().getName();
            myfilename = myfilename.replace(".", ":"); // replace '.' from the filename with ':'
            String[] str = myfilename.split(":"); // step through this filename using ':' prefix
            myfilename = str[0]; // get the filename without the file extension ex. .mp4
            myextension = str[1]; // get the extension only ex. .mp4
            txtSourceFile.setText(filechooser.getSelectedFile().getPath());
        }
    }
    
    private void selectPath(){
        filechooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int val = filechooser.showOpenDialog(this);
        if(val == JFileChooser.APPROVE_OPTION){
            txtSaveLocation.setText(filechooser.getSelectedFile().getAbsolutePath() + "\\"+ myfilename + txtFormat.getSelectedItem().toString());
        }
    }
    
