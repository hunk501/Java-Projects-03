    private void btnConvertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConvertActionPerformed
        // TODO add your handling code here:
        
        Path path = Paths.get("");
        String cur_path = path.toAbsolutePath().toString(); // get the working directory
        //String local_path = System.getProperty("sun.java.command");
        //local_path = local_path.replace("VideoConverter.jar", "");
        String ffmpeg = cur_path + "\\libffmpeg\\ffmpeg.exe";
        //String ffmpeg = "D:\\libffmpeg\\ffmpeg.exe";
        //  Check the ffmpeg.exe location is exists
        File file = new File(ffmpeg);
        if (!file.exists()) {
            JOptionPane.showMessageDialog(this, file.getAbsolutePath() +" was not found.!", "Error", JOptionPane.ERROR_MESSAGE);            
        } else {
            if( (txtSourceFile.getText().length() > 0) && (txtSaveLocation.getText().length() > 0) ){
                String filein = txtSourceFile.getText();
                String fileout = txtSaveLocation.getText();
                txtSourceFile.setText(myfilename +"."+ myextension);
                disableGUI();
                btnCancel.setEnabled(true);
                lblStatus.setText("Converting please wait...");
                // start converting..
                con = new Converter(ffmpeg, this, filein, fileout);
                Thread t = new Thread(con);
                t.start();
            } else {
                JOptionPane.showMessageDialog(this, "Some fields must be filled.!", "Error", JOptionPane.ERROR_MESSAGE);            
            }
        }
    }//GEN-LAST:event_btnConvertActionPerformed

